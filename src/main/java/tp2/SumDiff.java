package tp2;

public class SumDiff {
    int sum;
    int diff;

    public int getSum() {
        return sum;
    }

    public void setSum(int sum) {
        this.sum = sum;
    }

    public int getDiff() {
        return diff;
    }

    public void setDiff(int diff) {
        this.diff = diff;
    }

    @Override
    public String toString() {
        return "tp2.SumDiff{" +
                "sum=" + sum +
                ", diff=" + diff +
                '}';
    }
}
